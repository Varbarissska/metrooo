(function initTimeline() {
  const container = document.querySelector('.tl-container');
  if (!container) return;

  const items = container.querySelectorAll('.tl-item');
  const progress = container.querySelector('.tl-progress');
  const observerOptions = { threshold: 0.15, rootMargin: '0px 0px -50px 0px' };

  // 1. Появление карточек при скролле
  const appearObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        appearObserver.unobserve(entry.target);
      }
    });
  }, observerOptions);

  items.forEach(item => appearObserver.observe(item));

  // 2. Линия прогресса
  const updateProgress = () => {
    const rect = container.getBoundingClientRect();
    const windowHeight = window.innerHeight;
    const trackTop = rect.top + window.scrollY;
    const trackHeight = rect.height;
    
    // Сколько контейнера уже прошло через центр экрана
    let scrolled = (window.scrollY + windowHeight * 0.6) - trackTop;
    scrolled = Math.max(0, Math.min(scrolled, trackHeight));
    
    const percent = (scrolled / trackHeight) * 100;
    progress.style.height = `${percent}%`;
  };

  window.addEventListener('scroll', updateProgress, { passive: true });
  updateProgress();

  // 3. 3D-наклон карточек при наведении (лёгкий эффект)
  items.forEach(item => {
    const card = item.querySelector('.tl-card');
    card.addEventListener('mousemove', e => {
      const rect = card.getBoundingClientRect();
      const x = e.clientX - rect.left;
      const y = e.clientY - rect.top;
      const centerX = rect.width / 2;
      const centerY = rect.height / 2;
      
      const rotateX = ((y - centerY) / centerY) * -4; // наклон по X
      const rotateY = ((x - centerX) / centerX) * 4;  // наклон по Y
      
      card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) scale(1.02)`;
    });

    card.addEventListener('mouseleave', () => {
      card.style.transform = '';
    });
  });
})();

