(function initTrainTimeline() {
  const wrapper = document.querySelector('.tl-scroll-wrapper');
  if (!wrapper) return;

  // Скролл колёсиком мыши → горизонтально
  wrapper.addEventListener('wheel', (e) => {
    if (Math.abs(e.deltaY) > Math.abs(e.deltaX)) {
      e.preventDefault();
      wrapper.scrollLeft += e.deltaY * 1.5;
    }
  }, { passive: false });

  // Drag-and-drop скролл
  let isDown = false;
  let startX;
  let scrollLeft;

  wrapper.addEventListener('mousedown', (e) => {
    isDown = true;
    wrapper.style.cursor = 'grabbing';
    startX = e.pageX - wrapper.offsetLeft;
    scrollLeft = wrapper.scrollLeft;
  });

  wrapper.addEventListener('mouseleave', () => {
    isDown = false;
    wrapper.style.cursor = 'grab';
  });

  wrapper.addEventListener('mouseup', () => {
    isDown = false;
    wrapper.style.cursor = 'grab';
  });

  wrapper.addEventListener('mousemove', (e) => {
    if (!isDown) return;
    e.preventDefault();
    const x = e.pageX - wrapper.offsetLeft;
    const walk = (x - startX) * 1.8;
    wrapper.scrollLeft = scrollLeft - walk;
  });

  // Скрыть стрелку «→» когда прокрутили до конца
  const checkArrow = () => {
    const arrow = wrapper.querySelector('.tl-scroll-wrapper::after');
    const maxScroll = wrapper.scrollWidth - wrapper.clientWidth;
    if (wrapper.scrollLeft > maxScroll * 0.7) {
      wrapper.style.setProperty('--arrow-opacity', '0');
    } else {
      wrapper.style.setProperty('--arrow-opacity', '0.6');
    }
  };

  wrapper.addEventListener('scroll', checkArrow);
  checkArrow();

  // Авто-скролл до первого вагона при появлении
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        setTimeout(() => {
          wrapper.scrollTo({ left: 0, behavior: 'smooth' });
        }, 600);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.3 });

  observer.observe(wrapper);
})();

