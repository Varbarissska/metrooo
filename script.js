    function parseTime(str) {
        if (!str || str.includes("закрыта")) return null;
        let clean = str.replace(/[^\d-]/g, '').replace('-', ':');
        if (!clean.includes(':')) return null;
        const [h, m] = clean.split(':').map(Number);
        let total = h * 60 + m;
        return (h < 5) ? total + 1440 : total;
    }

     async function drawWave(lineId) {
    const config = metroLinesConfig[lineId];
    if (!config) return;

    let rawData = await d3.csv(config.file);

    // Подготовка данных
    let data = rawData.map(d => {
        const name = d["Наименование станции"]
            .replace(/\(.*?\)/gi, "")
            .replace(/Вестибюль \d+ станции /i, "")
            .trim();

        let openTime = null;
        let closeTime = null;

        const openMatch = (d["Время открытия"] || "").match(/(\d{1,2})[-:](\d{2})/);
        if (openMatch) openTime = parseInt(openMatch[1]) * 60 + parseInt(openMatch[2]);

        const closeMatch = (d["Время закрытия на вход"] || "").match(/(\d{1,2})[-:](\d{2})/);
        if (closeMatch) {
            let h = parseInt(closeMatch[1]);
            let m = parseInt(closeMatch[2]);
            closeTime = (h < 5 ? h + 24 : h) * 60 + m;
        }

        return { name, openTime, closeTime, openLabel: d["Время открытия"], closeLabel: d["Время закрытия на вход"] };
    }).filter(d => d.openTime !== null && d.name.length > 1);

    data.sort((a, b) => a.openTime - b.openTime);

    d3.select("#d3-chart-container").html("");

    // --- НОВЫЕ РАЗМЕРЫ ДЛЯ БОЛЬШОГО ОКНА ---
    const width = 1400; // Увеличили общую ширину холста
    const height = 750; 
    const radius = 180; // Чуть уменьшили радиус самих лепестков, чтобы осталось место для длинных названий
    const labelMargin = 60; // Увеличили отступ для названий

    const svg = d3.select("#d3-chart-container").append("svg")
        .attr("viewBox", `0 0 ${width} ${height}`) // Используем viewBox для адаптивности
        .attr("preserveAspectRatio", "xMidYMid meet")
        .style("width", "100%")
        .style("height", "100%");

    // Разносим центры графиков дальше друг от друга (на 20% и 80% ширины)
    const gOpen = svg.append("g").attr("transform", `translate(${width * 0.22}, ${height / 2 + 30})`);
    const gClose = svg.append("g").attr("transform", `translate(${width * 0.78}, ${height / 2 + 30})`);

    const angleScale = d3.scaleBand()
        .domain(data.map(d => d.name))
        .range([0, 2 * Math.PI])
        .padding(0.2);

    const rScale = d3.scaleLinear().domain([300, 1550]).range([40, radius]);

    function renderRose(group, timeKey, labelKey, color) {
        // Отрисовка лепестков
        group.selectAll("path")
            .data(data.filter(d => d[timeKey]))
            .enter()
            .append("path")
            .attr("fill", color)
            .attr("stroke", "#fff")
            .attr("stroke-width", 1)
            .attr("opacity", 0.7)
            .transition()
            .duration(800)
            .delay((d, i) => i * 30)
            .attrTween("d", function(d) {
                const i = d3.interpolate(40, rScale(d[timeKey]));
                return t => d3.arc()({
                    innerRadius: 40,
                    outerRadius: i(t),
                    startAngle: angleScale(d.name),
                    endAngle: angleScale(d.name) + angleScale.bandwidth()
                });
            });

        // Подписи времени (уменьшим шрифт, чтобы не мельтешило)
        group.selectAll(".time-text")
            .data(data.filter(d => d[timeKey]))
            .enter()
            .append("text")
            .attr("class", "time-text")
            .attr("text-anchor", "middle")
            .attr("font-size", "10px")
            .style("fill", "#444")
            .attr("transform", d => {
                const angle = angleScale(d.name) + angleScale.bandwidth() / 2 - Math.PI / 2;
                const r = rScale(d[timeKey]) + 12;
                return `translate(${Math.cos(angle) * r}, ${Math.sin(angle) * r})`;
            })
            .text(d => d[labelKey]);

        // Названия станций (интеллектуальный поворот)
        group.selectAll(".station-label")
            .data(data)
            .enter()
            .append("text")
            .attr("class", "station-label")
            .attr("font-size", "11px")
            .attr("font-weight", "500")
            .attr("transform", d => {
                const angle = angleScale(d.name) + angleScale.bandwidth() / 2 - Math.PI / 2;
                const r = radius + labelMargin;
                const x = Math.cos(angle) * r;
                const y = Math.sin(angle) * r;
                
                let rotation = (angle * 180 / Math.PI);
                // Если текст в левой части круга, переворачиваем его для читаемости
                if (angle > Math.PI / 2 || angle < -Math.PI / 2) {
                    // В D3 scaleBand углы могут идти иначе, проверяем диапазон:
                }
                const rawAngle = angleScale(d.name) + angleScale.bandwidth() / 2;
                if (rawAngle > Math.PI) rotation += 180;
                
                return `translate(${x}, ${y}) rotate(${rotation})`;
            })
            .attr("text-anchor", d => {
                const angle = angleScale(d.name) + angleScale.bandwidth() / 2;
                return (angle > Math.PI) ? "end" : "start";
            })
            .text(d => d.name);
    }

    renderRose(gOpen, "openTime", "openLabel", config.color);
    renderRose(gClose, "closeTime", "closeLabel", "#e74c3c");

    // Заголовки (позиционируем над центрами графиков)
    svg.append("text").attr("x", width * 0.22).attr("y", 60).attr("text-anchor", "middle").attr("class", "chart-title").text("Время открытия");
    svg.append("text").attr("x", width * 0.78).attr("y", 60).attr("text-anchor", "middle").attr("class", "chart-title").text("Закрытие на вход");

    document.getElementById('modal-title').innerText = `График работы: ${config.title}`;
}
